package main;

public class Variables {

	public static void main(String[] args) {
		
		/*
		 * 4 tipos de datos
		 * Caracteres
		 * Decimales
		 * Enteros
		 * Booleanos
		 * */
		
		
		//Declaracion de Variables
		int  version; 
		//Asignacion de valor
		version = 9;
		
		//Muestra en pantalla
		System.out.println(version);
		
		int versionJava = 10;
		
		System.out.println(versionJava);
		
		
		//Ejemplos de los tipos de datos
		
		//Ejemplo entero
		int versionJavita = 9;
		
		
		//Ejemplo decimal
		float versionEspecifica = 9.3f;
		
		System.out.println (versionEspecifica);
		
		//Ejemplo Caracter
		char letra = 'a';
		char numero = 97;
		
		System.out.println (letra);
		System.out.println (numero);
		
		//Ejemplo boolean
		boolean valor1 = true;
		boolean valor2 = false;
		
		System.out.println (valor1);
		System.out.println (valor2);
		
		
	
		//--------------------------------------
		
		int variable1 = 1, variable2= 2, variable3= 3;
		float pi = 3.1416f, e = 0.132f;
		
		System.out.println(variable1);
		System.out.println(variable2);
		System.out.println(variable3);
		
		//------------------------------------------
		
		final float pii;
		pii = 3.1415f;
		
		System.out.println(pii);
		
		
		
		
	}

}
