package main;

public class Main {

	public static void main(String[] args) {
		// Operador ternario
		//IFTTT then this that
		//?
		// expresion1 ? expresion2 : expresion3
		// Si expresion 1 = true entonces expresion2
		// Si expresion 1 = false entonces expresion3
		
		String mensaje = 10 > 9 ? "Es mayor" : "Es menor" ;
		
		System.out.println (mensaje);
		
		int enteros = false ? 1 : 2;
		System.out.println (enteros);

	}

}
